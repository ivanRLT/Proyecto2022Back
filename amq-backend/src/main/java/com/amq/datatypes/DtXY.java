package com.amq.datatypes;

public class DtXY {
	private String x;
	private double y;
	
	
	public DtXY() {	}
	
	public DtXY(String x, double y) {
		super();
		this.x = x;
		this.y = y;
	}
	public String getX() {
		return x;
	}
	public void setX(String x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	
	
}
