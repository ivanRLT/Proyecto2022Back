package com.amq.datatypes;

public class DtAnioMes {
	private Integer anio;
	private Integer mes;
	
	
	
	public DtAnioMes(Integer anio, Integer mes) {
		super();
		this.anio = anio;
		this.mes = mes;
	}
	public Integer getAnio() {
		return anio;
	}
	public void setAnio(Integer anio) {
		this.anio = anio;
	}
	public Integer getMes() {
		return mes;
	}
	public void setMes(Integer mes) {
		this.mes = mes;
	}
	
	
	
	
}
